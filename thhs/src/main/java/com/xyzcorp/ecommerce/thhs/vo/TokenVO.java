package com.xyzcorp.ecommerce.thhs.vo;

public class TokenVO {
	private String token;

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	@Override
	public String toString() {
		return "TokenVO [token=" + token + "]";
	}

}
